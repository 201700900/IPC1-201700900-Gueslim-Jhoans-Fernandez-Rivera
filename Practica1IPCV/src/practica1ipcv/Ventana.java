    private void excepciones(java.awt.event.KeyEvent evento) {

        char validar = evento.getKeyChar();
        try {
            String val = String.valueOf(evento.getKeyChar());

            if (!(val.matches("[0-9]"))&&validar != KeyEvent.VK_SPACE && validar!=KeyEvent.VK_BACK_SPACE) {

                getToolkit().beep();
                evento.consume();

                JOptionPane.showMessageDialog(rootPane, "Ingresar s칩lo n칰meros enteros positivos","Error!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (HeadlessException ex) {

        }
    }

    private void camposVacios() {
        try {
            if ((jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty())) {
                // cadena no est치 vac칤a
                
                getToolkit().beep();
                
                JOptionPane.showMessageDialog(rootPane, "No dejar campos Vac칤os", "Error!", JOptionPane.ERROR_MESSAGE);

            } else {
                jButton1.setEnabled(false);
            }
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(this, "Intente de nuevo", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }

